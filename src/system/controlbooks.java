   public void books_loading() {
    int c2;
    
    ResultSet rst;
    String query="select * from books";
    try{
         ps=MyConnection.getConnection().prepareStatement(query);
        rst=ps.executeQuery();
        ResultSetMetaData rsd=rst.getMetaData();
        c2=rsd.getColumnCount();
        
        DefaultTableModel d2 = (DefaultTableModel)bookTable.getModel();
        d2.setRowCount(0);
        while(rst.next()){
            Vector v4=new Vector();
            for (int i=1;i<=c2;i++){
                v4.add(rst.getString("id"));
                v4.add(rst.getString("ISBN"));
                v4.add(rst.getString("Title"));
                v4.add(rst.getString("Author"));
                v4.add(rst.getString("Genre"));
                v4.add(rst.getString("Category"));
                v4.add(rst.getString("Publisher"));
                v4.add(rst.getString("DatePublished"));
                v4.add(rst.getString("Synopsis"));
                v4.add(rst.getString("image"));   
            }
            d2.addRow(v4);
        }
    }catch (SQLException ex) {
            Logger.getLogger(addb.class.getName()).log(Level.SEVERE, null, ex);
        }
}
